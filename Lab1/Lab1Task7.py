"""

7. За довжинами двох сторін деякого трикутника і кутом між ними знайти довжину третьої сторони і
площу цього трикутника.

Єзелевич Ю.

"""


import math


def length(a, b, c) -> float:
    s = (a**2)+(b**2)-(2*a*b*math.cos(c))
    return math.sqrt(s)


a = float(input("Длина первой стороны треугольника = "))
b = float(input("Длина второй стороны треугольника = "))
c = float(input("Угол между сторонами = "))
print("Длина третьей стороны = ", length(a, b, c))
