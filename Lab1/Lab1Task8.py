def candies(m1, c, m2):
    p1 = c/m1
    p2 = p1 * m2
    print("Стоимость 1 кг конфет = ", p1)
    print("Стоимость", m2, "кг конфет = ", p2)


m1 = float(input("Масса конфет (кг) = "))
c = float(input("Стоимость конфет =  "))
m2 = float(input("Вторая масса конфет (кг) = "))
candies(m1, c, m2)
